/// ZuriTrails Design System - Spacing
/// 8px grid system for consistent spacing throughout the app
class AppSpacing {
  // ==================== BASE SPACING UNITS ====================
  /// Extra small spacing - 4px
  /// Usage: Dense layouts, icon padding
  static const double xs = 4.0;

  /// Small spacing - 8px
  /// Usage: Compact spacing, chip padding
  static const double sm = 8.0;

  /// Medium spacing - 16px
  /// Usage: Default spacing, card padding, element margins
  static const double md = 16.0;

  /// Large spacing - 24px
  /// Usage: Section spacing, large margins
  static const double lg = 24.0;

  /// Extra large spacing - 32px
  /// Usage: Screen padding, major sections
  static const double xl = 32.0;

  /// Extra extra large spacing - 48px
  /// Usage: Hero sections, major separators
  static const double xxl = 48.0;

  // ==================== SPECIFIC USE CASES ====================
  /// Card padding - 16px
  static const double cardPadding = md;

  /// Screen horizontal padding - 16px
  static const double screenHorizontal = md;

  /// Screen vertical padding - 24px
  static const double screenVertical = lg;

  /// List item spacing - 12px
  static const double listItemSpacing = 12.0;

  /// Section spacing - 32px
  static const double sectionSpacing = xl;

  /// Bottom sheet padding - 24px
  static const double bottomSheetPadding = lg;

  /// Modal padding - 24px
  static const double modalPadding = lg;

  // ==================== COMPONENT SPACING ====================
  /// Button padding horizontal - 24px
  static const double buttonPaddingH = lg;

  /// Button padding vertical - 16px
  static const double buttonPaddingV = md;

  /// Input padding horizontal - 16px
  static const double inputPaddingH = md;

  /// Input padding vertical - 18px
  static const double inputPaddingV = 18.0;

  /// Chip padding horizontal - 16px
  static const double chipPaddingH = md;

  /// Chip padding vertical - 8px
  static const double chipPaddingV = sm;

  /// Icon padding - 8px
  static const double iconPadding = sm;

  /// Avatar spacing - 8px
  static const double avatarSpacing = sm;
}
